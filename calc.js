function init() {
    var num = document.getElementById("num");
    num.value = 0;
    num.disabled = "disabled";
}

function num_1_click() {
    var num = document.getElementById("num");
    var n = num.value;
    //清除前导的数字0
    // if (n == "0") {
    //     n = "1";
    // } else {
    //     n = n + "1";
    // }
    n = n + "1";
    document.getElementById("num").value = n * 1;
}

